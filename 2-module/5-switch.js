const grade= "C"
switch (grade) {
    case "A":
        console.log("Excellent")    
        break;
    case "B":
        console.log("Very good")
        break;
    case "C":
        console.log("Good")    
        break;
    case "D":
        console.log("Pass")
        break
    case "E":
        console.log("Fair")    
        break;
    case "F":
        console.log("Fail")       
         break;   
    default:
        console.log("Grade not found")
        break;
}